public class AreaCalcMethods {

    public void triangleArea(int height, int base) {
        System.out.println("triangle area is : " + 0.5 * height * base);
    }


    public void squareArea(int side) {
        System.out.println("square area is : " + side * side);
    }


    public void rectangleArea(int lenght, int base) {
        System.out.println("rectangle area is : " + lenght * base);
    }

    public void circleArea(int radius) {
        System.out.println("circle area is : " + radius * radius);
    }


}